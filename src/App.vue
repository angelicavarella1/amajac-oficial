<script>
import HeaderComponent from './components/HeaderComponent.vue'
import HeroSection from './components/HeroSection.vue'
import QuemSomosSection from './components/QuemSomosSection.vue'
import NoticiasSection from './components/NoticiasSection.vue'
import EventosSection from './components/EventosSection.vue'
import GaleriaSection from './components/GaleriaSection.vue'
import ColaboradoresSection from './components/ColaboradoresSection.vue'
import InfoTempoRelogio from './components/InfoTempoRelogio.vue'
import ContatoSection from './components/ContatoSection.vue'
import FooterComponent from './components/FooterComponent.vue'

export default {
  name: 'App',
  components: {
    HeaderComponent,
    HeroSection,
    QuemSomosSection,
    NoticiasSection,
    EventosSection,
    GaleriaSection,
    ColaboradoresSection,
    InfoTempoRelogio,
    ContatoSection,
    FooterComponent
  },
  data() {
    return {
      mostrarBotaoTopo: false,
      online: navigator.onLine
    }
  },
  mounted() {
    window.addEventListener('scroll', this.handleScroll)
    window.addEventListener('online', this.updateOnlineStatus)
    window.addEventListener('offline', this.updateOnlineStatus)
  },
  beforeUnmount() {
    window.removeEventListener('scroll', this.handleScroll)
    window.removeEventListener('online', this.updateOnlineStatus)
    window.removeEventListener('offline', this.updateOnlineStatus)
  },
  methods: {
    handleScroll() {
      this.mostrarBotaoTopo = window.scrollY > 600
    },
    voltarAoTopo() {
      window.scrollTo({
        top: 0,
        behavior: 'smooth'
      })
    },
    updateOnlineStatus() {
      this.online = navigator.onLine
    }
  }
}
</script>